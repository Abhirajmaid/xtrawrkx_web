// Export individual arrays for different use cases
export const upcomingEvents = eventsData.filter(event => {
    const eventDate = new Date(event.date.replace(/(\d+)(st|nd|rd|th)/, '$1').replace(' 2025', ', 2025'));
    return eventDate > new Date();
});

export const pastEvents = eventsData.filter(event => {
    const eventDate = new Date(event.date.replace(/(\d+)(st|nd|rd|th)/, '$1').replace(' 2025', ', 2025'));
    return eventDate <= new Date();
});

// Helper function to get event by slug
export const getEventBySlug = (slug) => {
    return eventsData.find(event => event.slug === slug);
};

// Helper function to get related events
export const getRelatedEvents = (currentEventSlug, category, limit = 3) => {
    return eventsData
        .filter(event => event.slug !== currentEventSlug && event.category === category)
        .slice(0, limit);
}; 