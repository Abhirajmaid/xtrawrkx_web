// Helper functions
export const getJobBySlug = (slug) => {
    return jobsData.find(job => job.slug === slug);
};

export const getFeaturedJobs = () => {
    return jobsData.filter(job => job.featured);
};

export const getJobsByDepartment = (department) => {
    if (department === "All Departments") return jobsData;
    return jobsData.filter(job => job.department === department);
};

export const getJobsByLocation = (location) => {
    if (location === "All Locations") return jobsData;
    return jobsData.filter(job => job.location.includes(location));
};

export const getRecentJobs = (count = 3) => {
    return jobsData
        .sort((a, b) => new Date(b.posted) - new Date(a.posted))
        .slice(0, count);
};

export const getUrgentJobs = () => {
    return jobsData.filter(job => job.urgent);
};

export const getJobStats = () => {
    return {
        total: jobsData.length,
        featured: getFeaturedJobs().length,
        urgent: getUrgentJobs().length,
        departments: [...new Set(jobsData.map(job => job.department))].length,
        locations: [...new Set(jobsData.flatMap(job => job.location.split(' / ')))].length
    };
};

